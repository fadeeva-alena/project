function XMLNameEncode($strValue)
{	
	$search=array(" ","#","'","/","\\","(",")",",","[","]","+","\"","-","_","|","}","{","=");
	return str_replace($search,"",$strValue);
}

function PrepareForExcel($str)
{
	$ret = htmlspecialchars($str);
	if (substr($ret,0,1)== "=") 
		$ret = "&#61;".substr($ret,1);
	return $ret;

}




function ExportToPDF()
{
	global $nPageSize,$rs,$strTableName,$conn;
		global $colwidth,$leftmargin;
	if(!($row=db_fetch_array($rs)))
		return;


	include("libs/fpdf.php");

	class PDF extends FPDF
	{
	//Current column
		var $col=0;
	//Ordinate of column start
		var $y0;
		var $maxheight;

	function AcceptPageBreak()
	{
		global $colwidth,$leftmargin;
		if($this->y0+$this->rowheight>$this->PageBreakTrigger)
			return true;
		$x=$leftmargin;
		if($this->maxheight<$this->PageBreakTrigger-$this->y0)
			$this->maxheight=$this->PageBreakTrigger-$this->y0;
		$this->Rect($x,$this->y0,$colwidth["Status"],$this->maxheight);
		$x+=$colwidth["Status"];
		$this->Rect($x,$this->y0,$colwidth["Message"],$this->maxheight);
		$x+=$colwidth["Message"];
		$this->Rect($x,$this->y0,$colwidth["Mode"],$this->maxheight);
		$x+=$colwidth["Mode"];
		$this->Rect($x,$this->y0,$colwidth["ID"],$this->maxheight);
		$x+=$colwidth["ID"];
		$this->maxheight = $this->rowheight;
//	draw frame	
		return true;
	}

	function Header()
	{
		global $colwidth,$leftmargin;
	    //Page header
		$this->SetFillColor(192);
		$this->SetX($leftmargin);
		$this->Cell($colwidth["Status"],$this->rowheight,"Status",1,0,'C',1);
		$this->Cell($colwidth["Message"],$this->rowheight,"Message",1,0,'C',1);
		$this->Cell($colwidth["Mode"],$this->rowheight,"Mode",1,0,'C',1);
		$this->Cell($colwidth["ID"],$this->rowheight,"ID",1,0,'C',1);
		$this->Ln($this->rowheight);
		$this->y0=$this->GetY();
	}

	}

	$pdf=new PDF();

	$leftmargin=5;
	$pagewidth=200;
	$pageheight=290;
	$rowheight=5;


	$defwidth=$pagewidth/4;
	$colwidth=array();
    $colwidth["Status"]=$defwidth;
    $colwidth["Message"]=$defwidth;
    $colwidth["Mode"]=$defwidth;
    $colwidth["ID"]=$defwidth;
	
	$pdf->AddFont('CourierNewPSMT','','courcp1252.php');
	$pdf->rowheight=$rowheight;
	
	$pdf->SetFont('CourierNewPSMT','',8);
	$pdf->AddPage();
	

	$i=0;
	while((!$nPageSize || $i<$nPageSize) && $row)
	{
		$pdf->maxheight=$rowheight;
		$x=$leftmargin;
		$pdf->SetY($pdf->y0);
		$pdf->SetX($x);
		$pdf->MultiCell($colwidth["Status"],$rowheight,GetData($row,"Status",""));
		$x+=$colwidth["Status"];
		if($pdf->GetY()-$pdf->y0>$pdf->maxheight)
			$pdf->maxheight=$pdf->GetY()-$pdf->y0;
		$pdf->SetY($pdf->y0);
		$pdf->SetX($x);
		$pdf->MultiCell($colwidth["Message"],$rowheight,GetData($row,"Message",""));
		$x+=$colwidth["Message"];
		if($pdf->GetY()-$pdf->y0>$pdf->maxheight)
			$pdf->maxheight=$pdf->GetY()-$pdf->y0;
		$pdf->SetY($pdf->y0);
		$pdf->SetX($x);
		$pdf->MultiCell($colwidth["Mode"],$rowheight,GetData($row,"Mode",""));
		$x+=$colwidth["Mode"];
		if($pdf->GetY()-$pdf->y0>$pdf->maxheight)
			$pdf->maxheight=$pdf->GetY()-$pdf->y0;
		$pdf->SetY($pdf->y0);
		$pdf->SetX($x);
		$pdf->MultiCell($colwidth["ID"],$rowheight,GetData($row,"ID",""));
		$x+=$colwidth["ID"];
		if($pdf->GetY()-$pdf->y0>$pdf->maxheight)
			$pdf->maxheight=$pdf->GetY()-$pdf->y0;
//	draw fames
		$x=$leftmargin;
		$pdf->Rect($x,$pdf->y0,$colwidth["Status"],$pdf->maxheight);
		$x+=$colwidth["Status"];
		$pdf->Rect($x,$pdf->y0,$colwidth["Message"],$pdf->maxheight);
		$x+=$colwidth["Message"];
		$pdf->Rect($x,$pdf->y0,$colwidth["Mode"],$pdf->maxheight);
		$x+=$colwidth["Mode"];
		$pdf->Rect($x,$pdf->y0,$colwidth["ID"],$pdf->maxheight);
		$x+=$colwidth["ID"];
		$pdf->y0+=$pdf->maxheight;
		$i++;
		$row=db_fetch_array($rs);
	}
	$pdf->Output();
}

